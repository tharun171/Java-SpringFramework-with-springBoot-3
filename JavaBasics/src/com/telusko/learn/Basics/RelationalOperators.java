package com.telusko.learn.Basics;

public class RelationalOperators {

	public static void main(String[] args) {
		
		int a = 9;
		int b = 5;
		
		//Relations
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a>=b);
		System.out.println(a<=b);
		
		boolean c = true;
		System.out.println(!c);
		//not operator = makes true to false sand viceversa
	}

}
